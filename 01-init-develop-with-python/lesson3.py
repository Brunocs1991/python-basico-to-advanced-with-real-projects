"""
python = programming language
type of typing = dynamic typing
str -> string -> text
Strings are textual data, which is a sequence of characters in quotation marks.
"""

# Simple quotation marks
print('Hello World!')

# double quotation marks
print("Hello World!")

# Escaping characters
print('Hello \'World\'!')
print("Hello \"World\"!")

# r
print(r'Hello \'World\'!')
print(r"Hello \"World\"!")

print('Hello "World"!')
print("Hello 'World'!")
