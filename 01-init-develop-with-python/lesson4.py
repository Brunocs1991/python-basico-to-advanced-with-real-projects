# type int and float
# int = integer = whole number
# the int type is used to represent whole numbers, both positive and negative.
# int no sign = positive number
print(11)
print(-11)
print(0)

# float = floating point number = decimal number
# the float type is used to represent decimal numbers, both positive and negative.
# float no sign = positive number
print(11.0)
print(-11.0)
print(0.0)


# the function type() is used to determine the type of a variable.
print(type(11))  # int
print(type(11.0))  # float

