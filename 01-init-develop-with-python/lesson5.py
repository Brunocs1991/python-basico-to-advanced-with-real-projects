# type data bool (boolean)
# exists only two values: True and False
# boolean values are used to represent the truth value of an expression

print(10 == 10) # True
print(10 == 20) # False
print(10 != 20) # True
print(10 != 10) # False
print(10 > 20) # False
print(10 < 20) # True
print(10 >= 20) # False
print(10 <= 20) # True
print(10 > 10) # False
print(10 < 10) # False
print(10 >= 10) # True
print(10 <= 10) # True
print(10 == 10.0) # True
print(10 == 10.1) # False
print(10 == "10") # False