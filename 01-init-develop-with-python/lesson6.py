# conversion of types, casting
# type conversion, casting, type coercion
#  its act of converting a value from one type to another
# imutable types and primitives:
# int, float, bool, str

print(1 + 1)  # 2
print('a' + 'b')  # ab
print(int('1') + 1)  # 2
print(type(float('1') + 1))  # <class 'float'> 2.0
print(bool(' ')) # True
print(bool('')) # False
print(bool(0)) # False
print(bool(1)) # True
